    override fun likeById(id: Long) {
        posts = posts.map { post ->
            if (post.id == id) {
                val liked = !post.likedByMe
                post.copy(likedByMe = liked, likes = post.likes + if (liked) 1 else -1)
            } else {
                post
            }
        }
        data.value = posts
    }

    override fun shareById(id: Long) {
        posts = posts.map { post ->
            if (post.id == id) {
                post.copy(shares = post.shares + 1)
            } else {
                post
            }
        }
        data.value = posts
    }
